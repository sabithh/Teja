t0 = time.time()

for step in range(start_step, max_steps + 1):

    # ------------------------------------------------------------------
    # Evaluation + checkpoint
    # ------------------------------------------------------------------
    if step % eval_interval == 0:
        losses  = estimate_loss()
        elapsed = time.time() - t0
        lr_now  = get_lr(step)
        epoch   = step * batch_size / len(train_examples)

        print(f"  step {step:>5,} | epoch {epoch:.2f} | "
              f"train {losses['train']:.4f} | val {losses['val']:.4f} | "
              f"lr {lr_now:.1e} | {elapsed:.0f}s")

        # Save best checkpoint
        if losses['val'] < best_val_loss:
            best_val_loss = losses['val']
            save_checkpoint(BEST_CKPT, step, best_val_loss.item(), optimizer)
            print(f"   ✓ Best checkpoint saved (val {best_val_loss:.4f})")

    # Periodic save every N steps (safety net)
    if step > 0 and step % save_every == 0:
        save_checkpoint(PERIODIC_CKPT, step, best_val_loss, optimizer)
        print(f"   💾 Periodic checkpoint saved (step {step:,})")

    if step == max_steps:
        break

    # ------------------------------------------------------------------
    # LR update
    # ------------------------------------------------------------------
    lr = get_lr(step)
    for pg in optimizer.param_groups:
        pg['lr'] = lr

    # ------------------------------------------------------------------
    # Forward + backward
    # ------------------------------------------------------------------
    x, y = get_batch(train_examples)
    _, loss = model(x, y)

    optimizer.zero_grad(set_to_none=True)
    loss.backward()
    torch.nn.utils.clip_grad_norm_(model.parameters(), grad_clip)
    optimizer.step()



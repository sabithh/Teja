def prepare_sft_data(data_dir):
    """
    Download Stanford Alpaca dataset and format as conversation tokens.

    Each example is formatted as:
        [SYSTEM_TOKEN] system_text [EOT_TOKEN]
        [USER_TOKEN] user_text [EOT_TOKEN]
        [ASSISTANT_TOKEN] response_text [EOT_TOKEN]

    We create two arrays per example:
        input_ids: the full token sequence
        labels:    same as input_ids but -100 for non-assistant tokens
    """
    cache_path = os.path.join(data_dir, 'sft_data.json')

    if os.path.exists(cache_path):
        print(f"✓ SFT data already prepared: {cache_path}")
        with open(cache_path) as f:
            return json.load(f)

    print("\nPreparing Alpaca SFT dataset...")

    try:
        import tiktoken
        from datasets import load_dataset
        from tqdm import tqdm
    except ImportError:
        print("ERROR: Run: pip install tiktoken datasets tqdm")
        sys.exit(1)

    enc = tiktoken.get_encoding('gpt2')

    SYSTEM_TEXT = "You are Teja, a helpful AI assistant built from scratch."

    print("Downloading Stanford Alpaca dataset...")
    dataset = load_dataset("tatsu-lab/alpaca", split="train")
    print(f"  Loaded {len(dataset):,} examples")

    examples = []
    skipped  = 0

    for item in tqdm(dataset, desc="Formatting"):
        instruction = item['instruction'].strip()
        inp         = item['input'].strip()
        output      = item['output'].strip()

        if not instruction or not output:
            skipped += 1
            continue

        # Build user message
        if inp:
            user_text = f"{instruction}\n\n{inp}"
        else:
            user_text = instruction

        # Tokenize each part
        sys_ids   = [SYSTEM_TOKEN] + enc.encode(SYSTEM_TEXT)  + [EOT_TOKEN]
        user_ids  = [USER_TOKEN]   + enc.encode(user_text)    + [EOT_TOKEN]
        asst_ids  = [ASSISTANT_TOKEN] + enc.encode(output)    + [EOT_TOKEN]

        input_ids = sys_ids + user_ids + asst_ids

        # Labels: -100 for system+user tokens, real ids for assistant tokens
        labels = ([-100] * len(sys_ids) +
                  [-100] * len(user_ids) +
                  asst_ids)

        # Skip if too long for our context window
        if len(input_ids) > block_size:
            skipped += 1
            continue

        examples.append({
            'input_ids': input_ids,
            'labels':    labels,
        })

    print(f"\n  Formatted: {len(examples):,} examples")
    print(f"  Skipped:   {skipped:,} (too long or empty)")

    os.makedirs(data_dir, exist_ok=True)
    with open(cache_path, 'w') as f:
        json.dump(examples, f)
    print(f"  Saved to: {cache_path}")

    return examples



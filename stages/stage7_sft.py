def collate_batch(batch_examples):
    """
    Pad a batch of examples to the same length and stack into tensors.

    Padding uses -100 for labels (ignored by loss) and 0 for input_ids.
    """
    max_len    = max(len(e['input_ids']) for e in batch_examples)
    input_ids  = []
    labels     = []

    for e in batch_examples:
        pad_len = max_len - len(e['input_ids'])
        input_ids.append(e['input_ids'] + [0] * pad_len)
        labels.append(e['labels']    + [-100] * pad_len)

    return (
        torch.tensor(input_ids, dtype=torch.long),
        torch.tensor(labels,    dtype=torch.long),
    )


def get_batch(split_examples, bs=None):
    if bs is None:
        bs = batch_size
    idxs    = torch.randint(len(split_examples), (bs,))
    batch   = [split_examples[i] for i in idxs]
    x, y    = collate_batch(batch)
    return x.to(device), y.to(device)


@torch.no_grad()
def estimate_loss():
    out = {}
    model.eval()
    for name, split_data in [('train', train_examples), ('val', val_examples)]:
        losses = torch.zeros(eval_iters)
        for k in range(eval_iters):
            x, y = get_batch(split_data, bs=4)
            _, loss = model(x, y)
            losses[k] = loss.item()
        out[name] = losses.mean()
    model.train()
    return out


